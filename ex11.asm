Reset_Handler
	LDR R0,=SRC1
	LDR R1,=SRC2
	LDR R2,=RES
	MOV R3,#4
	LDR R8,=0X20000000
	MSR xPSR,R8
UP
	LDR R4,[R0],#4
	LDR R5,[R1],#4
	SBCS R6,R4,R5
	STR R6,[R2],#4
	SUB R3,#1
	TEQ R3,#0
	BNE UP 
	ADC R7,#0
	STR R7,[R2]
STOP 
	B STOP 
