;add 2 128 bit numbers 
	AREA RESET,DATA,READONLY 
	EXPORT __Vectors
__Vectors
	DCD 0x10001000;
	DCD Reset_Handler;
	ALIGN 
	AREA myarea,CODE , READONLY 
	EXPORT Reset_Handler
Reset_Handler 
	LDR R0,=SRC1
	LDR R1,=SRC2
	LDR R2,=RES
	MOV R3,#4
	ADDS R7,#0
UP 
	LDR R4,[R0],#4
	LDR R5,[R1],#4
	ADCS R6,R4,R5
	STR R6,[R2],#4
	SUB R3,1
	TEQ R3,#0
	BNE UP 
	ADC R7,#0
	STR R7,[R2]
STOP
	B STOP 
SRC1 DCD 0X734D,0XE43,0X87A,0XF51DC32A
SRC2 DCD 0XE4,0X534,0X21,0XEDA32938
	AREA MYDATA,DATA,READWRITE 
RES DCD 0,0,0,0,0
	END
	
	