	AREA RESET,DATA ,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000;stack pointer 
	DCD Reset_Handler;
	ALIGN 
	AREA mycode,CODE,READONLY
	ENTRY 
	EXPORT Reset_Handler
Reset_Handler
	MOV R3,#5
	LDR R0,=N
	LDR R1,=DST
BACK LDR R2,[R0],#4;
	STR R2,[R1],#4;
	SUB R3,#1
	CMP R3,#0
	BNE BACK 
STOP
	B STOP 
N	DCD 0X12345678,0X6A,0X234,0X49C,0XA13BC
	AREA mydata,DATA,READWRITE
DST	DCD 0,0,0,0,0
	
	END
	